    private void puntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_puntoActionPerformed

        String cadena_texto; // Definimos una variable de tipo String
        cadena_texto = Entrada.getText(); // Le asignamos a la variable lo que el textField contenga

        if (cadena_texto.length() <= 0) { // Comparamos que la cadena no esté vacía
            Entrada.setText("0."); // Si la cadena esta vacía, se inserta el valor establecido
        } else if (!calc.punto(Entrada.getText())) { // LLamado al método punto, si en patalla hay valores
            Entrada.setText(Entrada.getText() + ".");

        }
    }//GEN-LAST:event_puntoActionPerformed


    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        // TODO add your handling code here:

        String resultado; //Declaramos una variable de tipo string que guardara el resultado
        numero2 = Entrada.getText(); // La variable numero ya tiene valor, por ende proseguimos a asignarle a la variable numero2 el valor en pantalla

        if (!numero.equals("")) { // Condicionamos que la pantalla no este vacía
            // En la variable resultado guardamos lo que nos retorne el metodo operaciones:
            resultado = calc.operaciones(numero, numero2, signo);  // Envíamos como parémetros los valores
            Entrada.setText(resultado); // Finalmente mostramos en pantalla lo retornado
        }
    }//GEN-LAST:event_igualActionPerformed

    

