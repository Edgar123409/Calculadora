    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        // Borrar el ultimo caracter del textfield

        String cadena; //Declaración de la variable cadena de tipo String
        cadena = Entrada.getText(); // Se le asigna a cadena el valor del textfield

        if (cadena.length() > 0) { //Condicionamos que haya elementos en patalla
            // Si hay elementos, entonces borramos un caracter de pantalla
            Entrada.setText(Entrada.getText().substring(0, Entrada.getText().length() - 1));

        }
    }//GEN-LAST:event_borrarActionPerformed

