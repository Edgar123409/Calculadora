    private void productoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productoActionPerformed
        // Boton de multiplicacion

        if (!Entrada.getText().equals("")) {  // Si pantalla no esta vacía: 
            numero = Entrada.getText(); // Declaramos la variable numero y le asignamos el valor en pantalla
            signo = "x"; //Asignamos el signo. 
            Entrada.setText(""); // Mostramos la pantalla vacia
        }
    }//GEN-LAST:event_productoActionPerformed

